#ordenando listas
lista = [12, 10, 21, 33, 7]
lista_animal = ['viado', 'passarinho','cachorro','rato','gato','elefante']

print('Valores inseridos na lista: {}'.format(lista))
print('Valores inseridos na lista_animal: {}'.format(lista_animal))

lista.sort()
print('Lista ordenada: {}'.format(lista))

lista_animal.sort()
print('Lista_animal ordenada: {}'.format(lista_animal))

lista.reverse()
print('Lista reversa: {}'.format(lista))

lista_animal.reverse()
print('Lista_animal reversa: {}'.format(lista_animal))

